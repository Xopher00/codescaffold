def _derive_layers_contract(
    pkg_map: dict[str, set[str]],
    root_package: str,
) -> ContractSpec | None:
    layers = _topo_layers(pkg_map)
    if layers is None or len(layers) < 2:
        return None
    qualified = [[f"{root_package}.{sub}" for sub in layer] for layer in layers]
    return ContractSpec(name="package_layers", contract_type="layers", layers=qualified)


def _derive_independence_contracts(
    pkg_map: dict[str, set[str]],
    root_package: str,
) -> list[ContractSpec]:
    """
    Find the largest set of library packages where no two members directly import
    each other (maximum independent set in the pairwise-conflict graph).

    Entry-point packages (not imported by anything) are excluded because they are
    consumers of the library, not part of the architecture being enforced.
    """
    lib_pkgs = _library_packages(pkg_map)
    if len(lib_pkgs) < 2:
        return []

    # Conflict pairs: (A, B) if A directly imports B or B directly imports A
    conflicts: set[frozenset] = {
        frozenset([src, tgt])
        for src, tgts in pkg_map.items()
        for tgt in tgts
        if src in set(lib_pkgs) and tgt in set(lib_pkgs)
    }

    # Maximum independent set — brute-force is fine for N ≤ ~15
    best: list[str] = []
    for size in range(len(lib_pkgs), 1, -1):
        for subset in combinations(lib_pkgs, size):
            if all(frozenset([a, b]) not in conflicts for a, b in combinations(subset, 2)):
                best = sorted(subset)
                break
        if best:
            break

    if len(best) < 2:
        return []

    return [ContractSpec(
        name="independent_packages",
        contract_type="independence",
        modules=[f"{root_package}.{sub}" for sub in best],
    )]


def _derive_forbidden_contracts(
    view: ClusterView,
    source_root: Path,
    repo_root: Path,
    root_package: str,
) -> list[ContractSpec]:
    """Forbidden-import contracts from surprising cross-cluster connections."""
    label_to_file: dict[str, str] = {
        attrs.get("label", ""): attrs.get("source_file", "")
        for _, attrs in view.G.nodes(data=True)
        if attrs.get("label") and attrs.get("source_file")
    }

    contracts: list[ContractSpec] = []
    seen: set[tuple[str, str]] = set()

    for s in view.surprising_connections[:10]:
        source_files = s.get("source_files", [])
        if len(source_files) >= 2:
            src_file, tgt_file = str(source_files[0]), str(source_files[1])
        else:
            src_file = label_to_file.get(s.get("source", ""), "")
            tgt_file = label_to_file.get(s.get("target", ""), "")

        if not src_file or not tgt_file:
            continue

        src_pkg = _file_to_subpackage(Path(src_file), source_root, repo_root, root_package)
        tgt_pkg = _file_to_subpackage(Path(tgt_file), source_root, repo_root, root_package)

        if not src_pkg or not tgt_pkg or src_pkg == tgt_pkg:
            continue
        if _is_placeholder(src_pkg) or _is_placeholder(tgt_pkg):
            continue

        pair = (src_pkg, tgt_pkg)
        if pair in seen:
            continue
        seen.add(pair)

        short_src = src_pkg.split(".")[-1]
        short_tgt = tgt_pkg.split(".")[-1]
        name = f"no_{short_src}_to_{short_tgt}"
        contracts.append(ContractSpec(
            name=name,
            contract_type="forbidden",
            source_modules=[src_pkg],
            forbidden_modules=[tgt_pkg],
        ))

    return contracts


def _file_to_subpackage(
    file_path: Path, source_root: Path, repo_root: Path, root_package: str
) -> str | None:
    p = file_path if file_path.is_absolute() else (repo_root / file_path)
    try:
        rel = p.resolve().relative_to(source_root.resolve())
    except ValueError:
        return None
    parts = rel.parts
    if not parts:
        return None
    pkg_parts = [parts[0]]
    if len(parts) >= 2 and not parts[1].endswith(".py"):
        pkg_parts.append(parts[1])
    return ".".join(pkg_parts) if len(pkg_parts) >= 2 else None


def _is_placeholder(module_name: str) -> bool:
    return bool(re.search(r"\bpkg_\d{3}\b", module_name))


