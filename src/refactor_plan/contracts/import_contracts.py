# Rendering
# ---------------------------------------------------------------------------

def _render_contract(spec: ContractSpec) -> str:
    lines = [
        f"\n[importlinter:contract:{spec.name}]",
        f"name = {spec.name.replace('_', ' ')}",
        f"type = {spec.contract_type}",
    ]
    if spec.contract_type == "independence":
        lines.append("modules =")
        for m in spec.modules:
            lines.append(f"    {m}")
    elif spec.contract_type == "forbidden":
        lines.append("source_modules =")
        for m in spec.source_modules:
            lines.append(f"    {m}")
        lines.append("forbidden_modules =")
        for m in spec.forbidden_modules:
            lines.append(f"    {m}")
    elif spec.contract_type == "layers":
        lines.append("layers =")
        for i, layer in enumerate(spec.layers):
            for m in layer:
                lines.append(f"    {m}")
            if i < len(spec.layers) - 1:
                lines.append("    ---")
    return "\n".join(lines)


