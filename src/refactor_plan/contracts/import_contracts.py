# Lifecycle helpers
# ---------------------------------------------------------------------------

def _graph_mtime_iso(graph_json: Path) -> str:
    try:
        mtime = graph_json.stat().st_mtime
        return datetime.datetime.fromtimestamp(mtime).isoformat(timespec="seconds")
    except OSError:
        return "unknown"


def _is_hand_edited(config_path: Path) -> bool:
    if not config_path.exists():
        return False
    try:
        first_line = config_path.read_text(encoding="utf-8").splitlines()[:1]
    except OSError:
        return False
    return not (first_line and first_line[0].startswith(_PROVENANCE_MARKER))


def check_staleness(config_path: Path, graph_json: Path) -> tuple[bool, str]:
    """Return (is_stale, reason). Stale when graph is newer than stored mtime."""
    if not config_path.exists():
        return True, "No contracts file found — run contracts first."
    if _is_hand_edited(config_path):
        return False, "Contracts were hand-edited — staleness not tracked."
    current_mtime = _graph_mtime_iso(graph_json)
    for line in config_path.read_text(encoding="utf-8").splitlines():
        if "mtime:" in line:
            stored = line.split("mtime:", 1)[1].strip().rstrip(")")
            if stored != current_mtime:
                return True, f"Graph changed (stored {stored}, current {current_mtime})."
            return False, "Contracts are up to date."
    return True, "No mtime record — run contracts to refresh."


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def generate_contracts(
    refactor_plan: RefactorPlan,
    view: ClusterView,
    graph_json: Path,
    repo_root: Path,
    layout: ProjectLayout,
    force: bool = False,
) -> ContractArtifact:
    """Emit .importlinter contracts from current cluster structure."""
    config_path = repo_root / ".importlinter"
    mtime = _graph_mtime_iso(graph_json)

    if _is_hand_edited(config_path) and not force:
        return ContractArtifact(
            config_path=str(config_path),
            was_hand_edited=True,
            graph_mtime_iso=mtime,
            skipped_reason=(
                f"{config_path.name} exists and was not generated by codescaffold. "
                "Pass force=True to overwrite."
            ),
        )

    root_package = layout.root_package
    source_root = layout.source_root

    pkg_map = _build_package_map(root_package)
    cycles = _find_cycles(pkg_map)

    if cycles:
        logger.warning(
            "%d import cycle(s) detected — layers contract skipped. "
            "Run contracts to see cycle-break suggestions.",
            len(cycles),
        )

    layers = _derive_layers_contract(pkg_map, root_package) if not cycles else None
    independence = _derive_independence_contracts(pkg_map, root_package)
    forbidden = _derive_forbidden_contracts(view, source_root, repo_root, root_package)
    all_contracts = ([layers] if layers else []) + independence + forbidden

    cycle_suggestions = _analyze_cycles(
        cycles, pkg_map, view, source_root, repo_root, root_package
    )

    cycle_header_lines = (
        ["# Cycles detected (layers contract skipped):"]
        + [f"#   {' → '.join(c + [c[0]])}" for c in cycles[:5]]
        if cycles
        else []
    )

    header = "\n".join([
        f"{_PROVENANCE_MARKER} on {mtime}",
        f"# Graph: {graph_json.name} (mtime: {mtime})",
        "# DO NOT EDIT — run contracts to refresh",
    ] + cycle_header_lines + [
        "",
        "[importlinter]",
        "root_packages =",
        f"    {root_package}",
    ])

    contract_body = "".join(_render_contract(spec) for spec in all_contracts)
    config_path.write_text(header + contract_body + "\n", encoding="utf-8")

    return ContractArtifact(
        config_path=str(config_path),
        contracts=all_contracts,
        graph_mtime_iso=mtime,
        cycles_detected=cycles,
        cycle_break_suggestions=cycle_suggestions,
    )
