from __future__ import annotations

import datetime
import logging
import re
from itertools import combinations
from pathlib import Path
from typing import Literal

import grimp
import networkx as nx
from pydantic import BaseModel

from refactor_plan.interface.cluster_view import ClusterView
from refactor_plan.layout import ProjectLayout
from refactor_plan.planning.proposal import RefactorPlan

logger = logging.getLogger(__name__)

_PROVENANCE_MARKER = "# AUTO-GENERATED by codescaffold"


class ContractSpec(BaseModel):
    name: str
    contract_type: Literal["independence", "layers", "forbidden"]
    modules: list[str] = []
    source_modules: list[str] = []
    forbidden_modules: list[str] = []
    layers: list[list[str]] = []


class CycleBreakSuggestion(BaseModel):
    cycle: list[str]
    edge: str               # "pkg_a → pkg_b" — the edge to break
    cause: str              # which file/import causes the edge
    suggestion: str         # how to break it


class ContractArtifact(BaseModel):
    config_path: str
    contracts: list[ContractSpec] = []
    was_hand_edited: bool = False
    graph_mtime_iso: str = ""
    skipped_reason: str = ""
    cycles_detected: list[list[str]] = []
    cycle_break_suggestions: list[CycleBreakSuggestion] = []


# ---------------------------------------------------------------------------
# grimp helpers
# ---------------------------------------------------------------------------

def _build_package_map(root_package: str) -> dict[str, set[str]]:
    """Return subpackage → set of imported subpackages using grimp squashing."""
    try:
        g = grimp.build_graph(root_package)
    except Exception as exc:
        logger.warning("grimp failed to build graph for %s: %s", root_package, exc)
        return {}

    subpkgs = sorted({
        m.split(".")[1]
        for m in g.modules
        if m.count(".") >= 1 and m.startswith(root_package + ".")
    })

    for sub in subpkgs:
        try:
            g.squash_module(f"{root_package}.{sub}")
        except Exception:
            pass

    pkg_map: dict[str, set[str]] = {}
    for sub in subpkgs:
        full = f"{root_package}.{sub}"
        try:
            direct = g.find_modules_directly_imported_by(full)
        except Exception:
            direct = set()
        pkg_map[sub] = {
            d.split(".")[1]
            for d in direct
            if d.startswith(root_package + ".") and d.count(".") >= 1
        }

    return pkg_map


def _library_packages(pkg_map: dict[str, set[str]]) -> list[str]:
    """
    Packages imported by at least one other package — i.e., library modules, not
    entry points.  Entry points (mcp_server, demo, etc.) are top-of-chain consumers
    that import everything but nothing imports them.
    """
    imported_by_anyone = {tgt for tgts in pkg_map.values() for tgt in tgts}
    return sorted(pkg for pkg in pkg_map if pkg in imported_by_anyone)


def _find_cycles(pkg_map: dict[str, set[str]]) -> list[list[str]]:
    dag = nx.DiGraph()
    for src, tgts in pkg_map.items():
        for tgt in tgts:
            dag.add_edge(src, tgt)
    return list(nx.simple_cycles(dag))


def _topo_layers(pkg_map: dict[str, set[str]]) -> list[list[str]] | None:
    dag = nx.DiGraph()
    for src, tgts in pkg_map.items():
        for tgt in tgts:
            dag.add_edge(src, tgt)
    try:
        return [sorted(layer) for layer in nx.topological_generations(dag)]
    except nx.NetworkXUnfeasible:
        return None


# ---------------------------------------------------------------------------
# Cycle analysis — fuses grimp data with graphify community context
# ---------------------------------------------------------------------------

def _analyze_cycles(
    cycles: list[list[str]],
    pkg_map: dict[str, set[str]],
    view: ClusterView,
    source_root: Path,
    repo_root: Path,
    root_package: str,
) -> list[CycleBreakSuggestion]:
    """
    For each cycle edge, find the concrete import and cross-reference graphify
    community data to suggest which symbol to move or extract.
    """
    # Build a fresh (non-squashed) graph for import-level detail queries
    try:
        detail_graph = grimp.build_graph(root_package)
    except Exception:
        return []

    suggestions: list[CycleBreakSuggestion] = []
    seen_edges: set[tuple[str, str]] = set()

    for cycle in cycles:
        for i, src_sub in enumerate(cycle):
            tgt_sub = cycle[(i + 1) % len(cycle)]
            edge_key = (src_sub, tgt_sub)
            if edge_key in seen_edges:
                continue
            seen_edges.add(edge_key)

            src_full = f"{root_package}.{src_sub}"
            tgt_full = f"{root_package}.{tgt_sub}"

            # Find which specific module-level imports create this edge
            cause_lines: list[str] = []
            try:
                matches = detail_graph.find_matching_direct_imports(
                    f"{src_full}.* -> {tgt_full}.*"
                )
                for m in matches[:3]:
                    importer = m.get("importer", "").split(".")[-1]
                    imported = m.get("imported", "").split(".")[-1]
                    cause_lines.append(f"{importer}.py imports {imported}")
            except Exception:
                pass

            cause = "; ".join(cause_lines) or f"{src_sub} imports {tgt_sub}"
            suggestion_text = _suggest_cycle_break(
                src_sub, tgt_sub, cause, view, source_root, repo_root, root_package
            )

            suggestions.append(CycleBreakSuggestion(
                cycle=cycle,
                edge=f"{src_sub} → {tgt_sub}",
                cause=cause,
                suggestion=suggestion_text,
            ))
            if len(suggestions) >= 10:
                return suggestions

    return suggestions


def _suggest_cycle_break(
    src_sub: str,
    tgt_sub: str,
    cause: str,
    view: ClusterView,
    source_root: Path,
    repo_root: Path,
    root_package: str,
) -> str:
    """
    Use the graphify graph to determine whether the coupling is expected (same community)
    or surprising (different communities) and emit a concrete suggestion.
    """
    # Find all nodes in src_sub and tgt_sub packages from the graphify graph
    src_prefix = str(source_root / src_sub)
    tgt_prefix = str(source_root / tgt_sub)

    src_nodes = {
        nid for nid, attrs in view.G.nodes(data=True)
        if _node_in_package(attrs.get("source_file", ""), src_prefix, repo_root)
    }
    tgt_nodes = {
        nid for nid, attrs in view.G.nodes(data=True)
        if _node_in_package(attrs.get("source_file", ""), tgt_prefix, repo_root)
    }

    # Check if the importing node (src) and imported node (tgt) share a graphify community
    shared_community_pairs: list[str] = []
    for src_nid in src_nodes:
        src_comm = view.G.nodes[src_nid].get("community")
        for tgt_nid in tgt_nodes:
            tgt_comm = view.G.nodes[tgt_nid].get("community")
            if src_comm is not None and src_comm == tgt_comm:
                src_lbl = view.G.nodes[src_nid].get("label", src_nid)
                tgt_lbl = view.G.nodes[tgt_nid].get("label", tgt_nid)
                shared_community_pairs.append(f"{src_lbl} / {tgt_lbl}")

    if shared_community_pairs:
        pair_str = ", ".join(shared_community_pairs[:2])
        return (
            f"graphify places {pair_str} in the same community — "
            f"consider co-locating the imported symbol with its caller, "
            f"or extracting it to a shared module that neither {src_sub} nor {tgt_sub} "
            "owns (e.g. a models.py or types.py)."
        )

    return (
        f"graphify sees {src_sub} and {tgt_sub} as separate clusters. "
        f"Extract the shared symbol into a lower-level module (e.g. {root_package}.models or "
        f"{root_package}.types) that neither {src_sub} nor {tgt_sub} depends on."
    )


def _node_in_package(source_file: str, pkg_prefix: str, repo_root: Path) -> bool:
    if not source_file:
        return False
    p = Path(source_file)
    if not p.is_absolute():
        p = (repo_root / p).resolve()
    return str(p).startswith(pkg_prefix)


